<h3><b>WHO ARE WE?</b></h3>
<p>Majority of the women population in rural areas and splinter groups in towns and cities face discrimination even today despite the advancement in the society. They are oblivious to their rights and capabilities and are hence suppressed by the community.
<p>Awakening Eve is an upcoming non profit website for volunteers which targets eradicating lack of awareness among women .</p>

<h3><b>OBJECTIVE</b></h3>
<p>Awakening Eve aims to become an interface between the empowered and unempowered groups of women in the society enabling the fortunate to empower the unfortunate.
Through the website we hope to make the women self sufficient enabling them to defend themselves at times of injustice
</p>

<h3><b>METHODOLOGY</b></h3>
<p>It is achieved by using the concept of divide and conquer.We divide the areas of major concern and inspire a group of people who can volunteer to bring about upliftment of the women in that particular area who can in turn help in the development of other women, thereby bringing about chain reaction of getting inspired and inspiring others to achieve a holistic development.</p>

<h3><b>HOW CAN YOU CONTRIBUTE?</b></h3>
<p>You can join hands with us by registering as a volunteer in our website.You can post articles , create awareness and work together towards empowering women and build a better society.</p>
